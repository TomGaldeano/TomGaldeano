package claseU3;

import java.util.Scanner;

public class H2E23 {

	public static void main(String[] args) {
		int sum = 0, cont=0;
		Scanner entrada = new Scanner(System.in);
		System.out.println("Introduce numeros para la media y 0 para parar");
		int num = entrada.nextInt(); 
		while (num > 0) {
			cont++;
			sum+=num;
			System.out.println("Introduce numeros para la media y 0 para parar");
			num = entrada.nextInt(); 
		}
		if (sum>0 && num == 0) {
			System.out.println("La media es "+(double)sum/cont);
		} else {
			System.out.println("Ha habido un error");
		}

	}

}
